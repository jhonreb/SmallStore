    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        
        String [] usuarios={"admin","directora"};
       String [] contraseñas=new String[2];
       contraseñas[0]="peru123";
       contraseñas[1]="felicidad";
       String user=txtusuario.getText();
       String password=txtcontraseñaVisible.getText();
       boolean mensaje=false;
       for(int i=0;i<usuarios.length;i++){
           if(usuarios[i].equals(user)&&contraseñas[i].equals(password)){
              mensaje=true;
              JOptionPane.showMessageDialog(null,"Bienvenido "+user,"Acceso correcto",1);
              Menú frm2=new Menú();
              frm2.setVisible(true);
              dispose();
           }
       }
       if(!mensaje){
           JOptionPane.showMessageDialog(null,"Usuario o Contraseña incorrecta","ERROR!!",0);
       }
       
    }//GEN-LAST:event_btnIngresarActionPerformed

