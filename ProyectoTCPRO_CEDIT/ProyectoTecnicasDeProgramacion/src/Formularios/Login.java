    private void jLabel8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel8MouseClicked
        
        if(mostrar){
        txtcontraseñaVisible.setVisible(true);
        txtcontraseñaOculta.setVisible(false);
        txtcontraseñaVisible.setText(txtcontraseñaOculta.getText());
        mostrar=false;
        }else{
        txtcontraseñaVisible.setVisible(false);
        txtcontraseñaOculta.setVisible(true);
       txtcontraseñaOculta.setText(txtcontraseñaVisible.getText());
        mostrar=true;
        }
        
    }//GEN-LAST:event_jLabel8MouseClicked

