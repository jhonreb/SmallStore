    private void btnRegistrarApoderadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarApoderadoActionPerformed
    crear();
        if(registrar){
            try{
                if(txtNombresApoderado.getText().isEmpty() || txtApellidosApoderado.getText().isEmpty()
                    || txtDniApoderado.getText().isEmpty()||txtCelularApoderado.getText().isEmpty() || txtDireccionApoderado.getText().isEmpty()
                    || cboSexoApoderado.getSelectedIndex()==0){
                    JOptionPane.showMessageDialog(this,"Completa todos los datos");
                    return;
                }else{
                    apoderado= new ClassApoderado(getidApoderado(),getCelularApoderado(),getDireccionApoderado(),
                        getNombresApoderado(),getApellidosApoderado(), getDniApoderado(), getSexoApoderado());
                    apoderado.setIdApoderado(getidApoderado());
                    apoderado.setCelular(getCelularApoderado());
                    apoderado.setDireccion(getDireccionApoderado());
                    apoderado.setNombres(getNombresApoderado());
                    apoderado.setApellidos(getApellidosApoderado());
                    apoderado.setDni(getDniApoderado());
                    apoderado.setSexo(getSexoApoderado());
                    JOptionPane.showMessageDialog(this,"Apoderado registrado con el id: "+(getidApoderado()));
                    Principal.apoderados.add(apoderado);
                }
            }catch(Exception e){
                System.out.println(e.getMessage());
            }
        }
        else{
            try{
                modificar.setNombres(getNombresApoderado());
                modificar.setApellidos(getApellidosApoderado());
                modificar.setCelular(getCelularApoderado());
                modificar.setDireccion(getDireccionApoderado());
                modificar.setDni(getDniApoderado());
                //  modificar.setIdApoderado(getidApoderado());

                modificar.setSexo(getSexoApoderado());
                JOptionPane.showMessageDialog(this, "El apoderado se a modificado correctamente..");
            }catch(Exception e){
                System.out.println(e.getMessage());
            }
        }
        limpiarTextosApoderado();
        textosApoderado(false);
        controlesApoderado(true);
    }//GEN-LAST:event_btnRegistrarApoderadoActionPerformed

