    private void crear() {
        String archivo = getidApoderado() + ".txt";
        File CrearUbi = new File(CrearUbicacion);
        File CrearArchi = new File(CrearUbicacion + archivo);

        try {
            CrearUbi.mkdirs();
            Formatter CrearForma = new Formatter(CrearUbicacion + archivo);

            CrearForma.format("%s\r\n%s\r\n%s\r\n%s\r\n%s\r\n%s\r\n%s\r\n", "ID= " + getidApoderado(),
                    "NOMBRE= " + getNombresApoderado(), "APELLIDO= "
                    + getApellidosApoderado(), "CELULAR= " + getCelularApoderado(),
                    "DNI= " + getDniApoderado(), "DIRECCION= " + getDireccionApoderado(), "SEXO= " + getSexoApoderado());
            CrearForma.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Registro incorrecto");
        }

    }

    
